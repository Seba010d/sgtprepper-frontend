export const Div = () => {
  const element = document.createElement("div");
  return element;
};

export const Paragraph = () => {
  const element = document.createElement("p");
  return element;
};

export const Heading = (num = 1) => {
  const element = document.createElement(`h${num}`);
  return element;
};

